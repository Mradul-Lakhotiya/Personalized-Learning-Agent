// Package middleware provides HTTP middleware for the Go backend.
package middleware

import "net/http"

// CORS adds permissive CORS headers and handles OPTIONS pre-flight requests.
// In production, consider restricting Access-Control-Allow-Origin to your
// frontend domain instead of "*".
func CORS(next http.Handler) http.Handler {
	return http.HandlerFunc(func(w http.ResponseWriter, r *http.Request) {
		w.Header().Set("Access-Control-Allow-Origin", "*")
		w.Header().Set("Access-Control-Allow-Methods", "GET, POST, OPTIONS, PUT, DELETE")
		w.Header().Set("Access-Control-Allow-Headers", "Content-Type, Authorization")

		if r.Method == "OPTIONS" {
			w.WriteHeader(http.StatusOK)
			return
		}

		next.ServeHTTP(w, r)
	})
}
